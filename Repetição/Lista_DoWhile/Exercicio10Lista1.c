/*10) Fazer o programa para o algoritmo representado no fluxograma a seguir. Que
estrutura de repetição está representada neste fluxograma?*/
#include<stdio.h>
int main(void)
{
    int nr=0;

    while(nr<=100)
    {
        nr++;
        printf("%d\t", nr);
    }
    printf("\n\n THE END \n\n \t\t :D\t\t\n");
}
